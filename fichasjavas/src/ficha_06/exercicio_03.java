package ficha_06;

public class exercicio_03 {
    public static boolean par (int num)
    {
        if (num % 2 == 0){
            return true;
        }
        else
            return false;
    }
    public static boolean positivo (int num) {
        if (num >= 0)
            return true;
        else
            return false;
    }
}
